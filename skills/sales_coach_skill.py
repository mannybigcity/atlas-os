# skills/sales_coach_skill.py

def sales_coach(industry, situation):
    """
    Simple sales coaching skill.
    Returns sales advice based on industry and situation.
    """

    return f"""
SALES COACH ANALYSIS

Industry:
{industry}

Situation:
{situation}

Recommended Actions:

1. Build rapport first.
2. Ask discovery questions.
3. Identify pain points.
4. Present value.
5. Ask for the next step.

Suggested Question:

'What is your biggest challenge right now regarding {industry}?'

Focus on helping the customer solve a problem before discussing price.
"""